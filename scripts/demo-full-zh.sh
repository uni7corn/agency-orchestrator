type_text() {
  local text="$1"
  for (( i=0; i<${#text}; i++ )); do
    # Handle multi-byte UTF-8 characters
    local char="${text:$i:1}"
    local byte=$(printf '%d' "'$char" 2>/dev/null || echo 0)
    if [ "$byte" -ge 224 ] 2>/dev/null; then
      # 3-byte UTF-8 (Chinese characters)
      printf '%s' "${text:$i:3}"
      i=$((i+2))
      sleep 0.06
    elif [ "$byte" -ge 192 ] 2>/dev/null; then
      # 2-byte UTF-8
      printf '%s' "${text:$i:2}"
      i=$((i+1))
      sleep 0.05
    else
      printf '%s' "$char"
      sleep 0.04
    fi
  done
}

